    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create(Request $request)
    {
        //
        $data = $request->all();
        $validatedData = $request->validate([
                "email"  => "required",
                "comment"  => "required|min:5"
                ]);
            //process comment
            $add_comment = new Comment();
            $add_comment->user_id = $data['user_id'];
            $add_comment->post_id = $data['post_id'];
            $add_comment->email = $data['email'];
            $add_comment->comment = $data['comment'];
            if($add_comment->save()){ 
                    //return home
                    return redirect()->back()->with('status', 'Add Comment Successfully');
                }else{
                    //send back an error message
                    return redirect()->back()->withInput()->with('status', 'Unable to Add Comment at This Time');
                }
    }

